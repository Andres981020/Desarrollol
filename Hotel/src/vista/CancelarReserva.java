    private void cancelarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarRActionPerformed
        int reserv = Integer.valueOf(listReservDisp.getSelectedItem().toString());
        Reserva reservaN = usuario.consultarReserva(reserv);
        reservaN.setEstado("Cancelada");
        usuario.modificarReserva(reservaN);
        
        JOptionPane.showMessageDialog(this, "La reserva se ha cancelado satisfactoriamente");
        
        Recepcionist recep  = new Recepcionist();
        recep.setVisible(true);
        dispose();
        
    }//GEN-LAST:event_cancelarRActionPerformed

